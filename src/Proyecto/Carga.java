    public void run(){
        Thread current =  Thread.currentThread();

        while(current==hc){
            for (int i = 0; i < 100; i++) {
                try {
                    Thread.sleep(150);
                } catch (InterruptedException ex) {
                }
                barraPro.setValue(i);
                
                if(i>0){
                    txtBarra.setText("Espere");
                    barraPro.setStringPainted(true);
                    if(i>5 && i<10){
                        txtBarra.setText("Cargando Componentes.");
                        barraPro.setStringPainted(true);
                    }
                    if(i==10){
                        txtBarra.setText("Cargando Componentes.");
                        barraPro.setStringPainted(true);
                        try {
                            Thread.sleep(600);
                        } catch (Exception e) {
                        }
                        i=24;
                    }
                    if(i==25){
                        txtBarra.setText("Cargando Componentes...");
                        barraPro.setStringPainted(true);
                        try {
                            Thread.sleep(600);
                        } catch (Exception e) {
                        }
                        i=49;
                    }
                    
                    if(i==50){
                        txtBarra.setText("Cargando Componentes....");
                        barraPro.setStringPainted(true);
                        try {
                            Thread.sleep(600);
                        } catch (Exception e) {
                        }
                        i=74;
                    }
                    
                    if(i==75){
                        txtBarra.setText("Cargando Componentes.....");
                        barraPro.setStringPainted(true);
                        try {
                            Thread.sleep(800);
                        } catch (Exception e) {
                        }
                        i=89;
                    }
                    if(i==90){
                        txtBarra.setText("Iniciando AplicaciÃ³n..");
                        barraPro.setStringPainted(true);
                        try {
                            Thread.sleep(800);
                        } catch (Exception e) {
                        }
                        i=98;
                    }
                    if(i==99){
                        barraPro.setStringPainted(true);
                        i=100;
                        txtBarra.setText("Completado!!");
                        try {
                            Thread.sleep(800);
                        } catch (Exception e) {
                        }
                        
                    }
                }else{
                    txtBarra.setText(i + " %  Espere...");
                }
                try {
                    Thread.sleep(40);
                } catch (InterruptedException ex) {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
            setVisible(false);
//            VentanaPrincipal principal = new VentanaPrincipal();
//            principal.setVisible(true);
        }
        
    }
    
